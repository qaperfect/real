"use client"

import { useEffect, useState } from "react"
import { ChevronUp } from "lucide-react"

export default function ScrollToTop() {
  const [isVisible, setIsVisible] = useState(false)

  useEffect(() => {
    const toggleVisibility = () => {
      if (window.scrollY > 300) {
        setIsVisible(true)
      } else {
        setIsVisible(false)
      }
    }

    window.addEventListener("scroll", toggleVisibility)
    return () => window.removeEventListener("scroll", toggleVisibility)
  }, [])

  const scrollToTop = () => {
    window.scrollTo({ top: 0, behavior: "smooth" })
  }

  if (!isVisible) return null

  return (
    <button
      onClick={scrollToTop}
      className="fixed bottom-8 right-8 bg-blue-900 text-white p-4 rounded-full hover:bg-blue-800 active:bg-blue-950 shadow-lg hover:shadow-xl transition-all duration-300 z-40 group hover-lift"
      aria-label="Scroll to top"
    >
      <ChevronUp size={24} className="group-hover:-translate-y-1 transition-transform duration-300" />
    </button>
  )
}
